// ErrCheckOsExit checks os.Exit call in main function
var ErrCheckOsExit = &analysis.Analyzer{
	Name: "errcheckosexit",
	Doc:  "check os.Exit call in main function",
	Run:  run,
}

func run(pass *analysis.Pass) (interface{}, error) {
	for _, file := range pass.Files {
		ast.Inspect(file, func(n ast.Node) bool {
			if call, ok := n.(*ast.CallExpr); ok {
				if s, ok := call.Fun.(*ast.SelectorExpr); ok {
					if x, ok := s.X.(*ast.Ident); ok {
						if x.Name == "os" && s.Sel.Name == "Exit" {
							pass.Reportf(call.Pos(), "os.Exit called")
						}
					}
				}
			}

			return true
		})
	}

	return nil, nil
}

func main() {
	checks := []*analysis.Analyzer{
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		bools.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		httpresponse.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		printf.Analyzer,
		shift.Analyzer,
		shadow.Analyzer,
		stdmethods.Analyzer,
		structtag.Analyzer,
		tests.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		ErrCheckOsExit,
	}

	for _, v := range staticcheck.Analyzers {
		if strings.HasPrefix(v.Doc.Title, "SA") {
			checks = append(checks, v.Analyzer)
		}
	}

	multichecker.Main(checks...)
}
